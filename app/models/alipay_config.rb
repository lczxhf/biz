class AlipayConfig 
  include  BaseModel

  field :name, type:String, default:"支付宝手机支付"
  field :desc, type:String,  default:"支付宝手机支付"

  field :is_open, type:Boolean, default:false #on off

  field :account, type:String     #支付宝收款账户
  field :parter_id, type:String   #支付宝提供的合作者身份编码(以2088开头)
  field :sec_key, type:String     #支付宝提供的交易安全校验码  
  
end
